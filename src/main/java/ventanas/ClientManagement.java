    private void showDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_showDataActionPerformed
        // TODO add your handling code here:
        registerButton.setEnabled(false);
         int fila = Tabla.getSelectedRow();
        
        if (fila == -1 || inputID.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No se ha seleccionado un cliente.");
        } else {
       
        String consulta = "select * from cliente where clienteId=?";
    
        try {
    PreparedStatement ps = conexion.prepareStatement(consulta);
    ps.setInt(1, Integer.parseInt(inputID.getText()));
    ResultSet res = ps.executeQuery();
    
        if (res.next()) {
            client = new Cliente(
                res.getString("email"),
                res.getString("tel_cel"),
                res.getString("direccion"), 
                res.getString("colonia"), 
                Integer.parseInt(res.getString("zp")), 
                res.getString("nombre"), 
                res.getString("apellidoP"), 
                res.getString("apellidoM"), 
                res.getInt("clienteId"), 
                res.getString("tel_casa"));
            
            JOptionPane.showMessageDialog(this,client.showData());
                 }

        } catch(SQLException err) {
            System.out.println("Hubo un error para consultar los datos en modify de cliente: " + err);
        }
       }
    }//GEN-LAST:event_showDataActionPerformed

