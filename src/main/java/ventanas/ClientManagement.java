    private void query(){
        String sql = "select * from cliente";
        try {
            Statement st = conexion.createStatement();
            ResultSet rs = st.executeQuery(sql);
            Object[] cliente = new Object[5];
            modelo = (DefaultTableModel) Tabla.getModel();
            
            while(rs.next()){
                cliente [0] = rs.getInt("clienteId");
                cliente [1] = rs.getString("nombre");
                cliente [2] = rs.getString("apellidoP");
                cliente [3] = rs.getString("apellidoM");
                cliente [4] = rs.getString("tel_cel");
                
                modelo.addRow(cliente);
                
            }
            Tabla.setModel(modelo);
        }catch (SQLException e){
            System.out.println("Hubo un error para consultar los datos: " + e);
        }
    }
    
