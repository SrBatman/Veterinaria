    private void query(){
        String sql = "select * from empleado";
        try {
            Statement st = conexion.createStatement();
            ResultSet rs = st.executeQuery(sql);
            Object[] empleado = new Object[4];
            modelo = (DefaultTableModel) Tabla.getModel();
            
            while(rs.next()){
                empleado [0] = rs.getInt("empleadoId");
                empleado [1] = rs.getString("nombre");
                empleado [2] = rs.getString("apellidoP");
                empleado [3] = rs.getString("puesto");
                
                modelo.addRow(empleado);
                
            }
            Tabla.setModel(modelo);
        }catch (SQLException e){
            System.out.println("Hubo un error para consultar los datos: " + e);
        }
    }
    
